<h3>ポイント.comはどんなサイトなのか？</h3>
<p>ポイント.comは風俗メディア（媒体）の異端児として、革命的なシステムを採用し<br />
全国、全業種、全加盟店舗全てで現金として使用できる共通ポイントサイトです。</p>

<h3>ポイントに有効期限はあるの？</h3>
<p>ポイント有効期限は、マイページ最終ログインから２年間で無効となります。</p>

<h3>ポイントは、何ポイントから使用できるの？</h3>
<p>ポイントは１ポイント＝１円計算で、５００ポイントから、現金５００円分として使用できます。</p>

<h3>ポイント.comを使用するとどんなメリットがあるの？</h3>
<ol>
	<li>各加盟店舗の他サイト会員価格＆イベント価格（各店舗により条件あり）から、さらにポイント付与<br />
	されるので、どの風俗サイトよりも最安値で遊べます。</li>
	<li>１．で貰ったポイントは、全国どの加盟店でも現金として使用できます。</li>
	<li>今まであった各店舗の複数のポイントカードが一切不要になり<br />
	全ての店舗のポイントをサイト内のページで一括管理できます。</li>
	<li>今まで保管場所に困っていた、風俗店のポイントカードの不安から開放されます。</li>
</ol>

<h3>電話予約や入店の際にお店側から、貰えるはずのポイント分を現金割引すると言われた。</h3>
<p>このような申し出が店舗側からあった場合にユーザー様が申し出を受け、その場の現金割引を<br />
してもらい入店した事が発覚した場合、保有ポイントが全て無効になり、強制退会になります。</p>

<h3>ポイント.comのポイント使用・申請は２４時間使えるの？</h3>
<p>各店舗営業時間内であれば、２４時間使用可能です。但しサーバーメンテナンス中は使用できません。</p>

